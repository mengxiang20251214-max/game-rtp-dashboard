async function main() {
  console.log("🌱 开始播种数据（30 个游戏）...");

  // 清空旧数据
  await prisma.game.deleteMany();

  for (const g of games) {
    await prisma.game.create({
      data: {
        id: g.id,
        name: g.name,
        category: g.category,
        image: g.image,
        rtp: g.rtp,
        targetRtp: g.targetRtp,
        status: g.status,
        playerCount: g.playerCount,
        totalBets: g.totalBets,
        totalWins: g.totalWins,
        trend: JSON.stringify(g.trend),
        rank: g.rank,
        isActive: g.isActive,
        // 初始值（每日重置基准）= 种子值
        initialRank: g.rank,
        initialPlayerCount: g.playerCount,
        initialTotalBets: g.totalBets,
        initialTotalWins: g.totalWins,
      },
    });
    console.log(`  \u2713 #${g.rank} ${g.name} (${g.category})`);
  }

  console.log(`\u2705 完成，共插入 ${games.length} 个游戏。`);
}

main()
  .catch((e) => {
    console.error("\u274c 播种失败:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
